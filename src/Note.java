
public class Note {
	
	private int id;
	private float note1;
	public float note2;
	
	
	
	public Note() {
	
	}



	public Note(int id, float note1, float note2) {
	
		this.id = id;
		this.note1 = note1;
		this.note2 = note2;
	}



	public int getId() {
		return id;
	}



	public void setId(int id) {
		this.id = id;
	}



	public float getNote1() {
		return note1;
	}



	public void setNote1(float note1) {
		this.note1 = note1;
	}



	public float getNote2() {
		return note2;
	}



	public void setNote2(float note2) {
		this.note2 = note2;
	}
	
	
	
	

}
